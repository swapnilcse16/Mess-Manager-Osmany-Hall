    private void init(){
        conn = JConnection.ConnecrDb();
        
        this.setTitle("VIEW CANCELLED SEAT INFORMATION");
        tm = (DefaultTableModel) table.getModel();
        tm.setRowCount(0);
        
        try{
            psmt = conn.prepareStatement("SELECT * FROM previousstudents");
            rs = psmt.executeQuery();
            
            while(rs.next()){
                Object o[] = {rs.getInt(1)+"",rs.getString(3),rs.getString(2),rs.getString(6),rs.getString(16)};
                tm.addRow(o);
            }
            
            psmt.close();
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Oops! There are some problems!", "Unknown Error Occured!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment( JLabel.CENTER );
        table.setDefaultRenderer(Object.class, centerRenderer);
        
        int width[] = {100,200,150,100,100};
        for(int i=0; i<5; i++)  table.getColumnModel().getColumn(i).setPreferredWidth(width[i]);
        
        
        JFrame frame = this;
        frame.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);        
        frame.addWindowListener(new WindowAdapter() {
            public void windowClosing(WindowEvent evt) {
                try{
                    Menu m = new Menu();
                    m.setVisible(true);
                    frame.setVisible(false);
                    conn.close();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Oops! There are some problems!", "Unknown Error Occured!", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
    }

    
